        /// Диаметр основания ручки.
        /// </summary>
        HandleBaseDiameter,

        /// <summary>
        /// Диаметр ручки.
        /// </summary>
        HandleDiameter,

        /// <summary>
        /// Длина основания ручки.
        /// </summary>
        HandleBaseThickness,

        /// <summary>
        /// Длина ручки.
        /// </summary>
        HandleThickness,

        /// <summary>
        /// Ширина ручки.
        /// </summary>
        HandleRecWidth,

        /// <summary>
        /// Длина ручки.
        /// </summary>
        HandleRecHeight,

        /// <summary>
        /// Некорректный параметр.
        /// </summary>
        Unexpected,
    }
}
